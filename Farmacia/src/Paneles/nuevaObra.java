    private void Cargar(int oscod) {
        ObraSocial ob = new ObraSocial();
        ob = ob.ObtenerObrasMenosLaOscodDeParametro(oscod);
        ComboOB1.addItem(ob.getNombre());
        int tam = ob.getPlan().length;
        int i= 0;
        while(tam > 0)
        {
            comboPlanOB1.addItem(ob.getPlan()[i]);
            tam--;
            i++;
        }
    }
