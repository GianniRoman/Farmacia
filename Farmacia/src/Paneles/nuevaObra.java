public void CargarObraSocial()
  {
      ObraSocial[] obs = new ObraSocial[3];
      ObraSocial ob = new ObraSocial();
      obs = ob.traerObrasQueTiene(pModif.traerDni());
      int cantob = ob.ObtenerCantobDeUnCliente(pModif.traerDni());
      int cantobEnSistema = ob.contarObrasSociales();
      if(cantob != cantobEnSistema)
      {
            if(cantob == 0)
            {
                CargarObrasSociales();
            }else{
                if(obs[0] != null)
                {
                    int oscod = ob.ObtenerCcod(obs[0].getNombre());
                    Cargar(oscod); // cargar obras sociales menos la del oscod  
                }     
            }               
      }else{
          obLlenas.setVisible(true);
          labelNombre.setVisible(false);
          labelPlan.setVisible(false);
          ComboOB1.setVisible(false);
          comboPlanOB1.setVisible(false);
          buttonAgregarOb.setVisible(false);
      }
         
      
      
  }

public void CargarObrasSociales()
  {
      ObraSocial ob = new ObraSocial();
      int cant = ob.contarObrasSociales();
      while(cant > 0)
      {
          ob = ob.Cargar(cant);
          ComboOB1.addItem(ob.getNombre());
          cant--;
      }
      
      
  }
